for(let i=1;i<=8;i++){
    console.log(`Valor de i é ${i}`)
}

// uso do break
for(let i=1;i<=8;i++){
    if (i == 5){
        break
    }
    console.log(`Valor de i é ${i}`)
}

// uso do continue
for(let i=1;i<=8;i++){
    if (i == 5){
        continue
    }
    console.log(`Valor de i é ${i}`)
}